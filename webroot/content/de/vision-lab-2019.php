<div class="text-column">
    <p>
        Im Oderbruch, eine Stunde östlich von Berlin, ensteht rund um den Herzershof das erste UnaVillage.
        UnaVillages sind Zukunftslabore, in denen regional vernetzte Gemeinschaften zusammen leben und arbeiten.
        Im Rahmen eines Workshops am Herzershof wollen wir mit euch kreative und enkeltaugliche Visionen
        für den Ort entwickeln und zu Papier bringen.
    </p>
</div>

