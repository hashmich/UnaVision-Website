<div class="text-column">
    <h2>Was</h2>
    <p>
        Gefragt sind Wohnkonzepte für die Haus- und Ortsgemeinschaft, aber auch für Besucher.
        Das Grundstück der Gemeinschaft, die benachbarten Grundstücke von 20ha und auch 4 Plattenbauten
        in der Nachbarschaft können vielfältig genutzt werden, zum Beispiel Coworking-Spaces, Werkstätten,
        Dorfläden, Begegnungscafes. Auf dem Grundstück stehen Gewächshäuser und Flächen zur Gestaltung
        nach Permakulturprinzipien zur Verfügung.
    </p>
    <h2>Wer</h2>
    <p>
        Eingeladen sind Anwohner und Interessierte unterschiedlichster Fachdrichtungen, Künstler,
        Architekten, Landschfatsplaner und Visionäre.
        Die Teilnehmerzahl ist auf 20 Leute begrenzt.
    </p>
    <h2>Präsentation</h2>
    <p>
        Eine öffentliche Präsentation der Ergebnisse findet statt am Samstag, 9. März.
