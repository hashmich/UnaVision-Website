    <h1>Visions-Labor Herzershof</h1>
    <p class="slogan">1. - 10. März 2019</p>
    <hr>
</div>

<div class="text-column">
    <p>
        Im Oderbruch, eine Stunde östlich von Berlin, ensteht rund um den Herzershof das erste Unavillage.
        UnaVillages sind Zukunftslabore, in denen regional vernetzte Gemeinschaften zusammen leben und arbeiten.
        Im Rahmen eines Workshops am Herzershof wollen wir mit euch kreative und enkeltaugliche Visionen
        für den Ort entwickeln und zu Papier bringen.
    </p>
    <p>
        Gefragt sind Wohnkonzepte für die Haus- und Ortsgemeinschaft, aber auch für Besucher.
        Das Grundstück der Gemeinschaft, die benachbarten Grundstücke von 20ha und auch 4 Plattenbauten
        in der Nachbarschaft können vielfältig genutzt werden, zum Beispiel Coworking-Spaces, Werkstätten,
        Dorfläden, Begegnungscafes. Auf dem Grundstück stehen Gewächshäuser und Flächen zur Gestaltung
        nach Permakulturprinzipien zur Verfügung.
    </p>
    <p>
        Eingeladen sind Anwohner und Interessierte unterschiedlichster Fachdrichtungen, Künstler,
        Architekten, Landschfatsplaner und Visionäre.
    </p>
    <p>
        Die Teilnehmerzahl ist auf 20 Leute begrenzt.<br>
        Anmeldung bitte unter dieser
        <a href="https://docs.google.com/forms/d/e/1FAIpQLScSMwrcsp66jx9UaxMa7khwSKv6Anf79S-cDjJd33LWhouNCA/viewform" target="_blank">Adresse</a>
        (Google Form).
    </p>
    <p>
        Die Anreise erfolgt am Freitag, dem 1. März.
        Während des Wochenendes haben die Teilnehmer genügend Zeit, um sich und die Umgebung
        kennen zu lernen. In der darauf folgenden Woche bis Sonntag, 10. März,
        kann interdisziplinär, offen und inspiriert an Visionen für den Ort gearbeitet werden.
    </p>
    <p>
        Eine öffentliche Präsentation der Ergebnisse findet statt am Samstag, 9. März.
    </p>

</div>