<div class="text-column">
    <p>
        Die Initiative zu UnaVision wurde von der gemeinnützigen Genossenschaft
        ThinkCamp eG. ins Leben gerufen.
        UnaVision ist entstanden als die gemeinsame Vision vieler Menschen,
        die in zahlreichen von ThinkCamp international organisierten Workshops und Lernreisen
        konzeptuell erarbeitet wurde.
    </p>

    <h2>Vision</h2>
    <p>
        Unsere Vision ist, allen Menschen ein gutes und zufriedenes Leben zu ermöglichen.
        Die Pflege und Nutzung unseres lebendigen Planeten zur
        Erhaltung unserer Lebensgrundlagen zählt zu unseren wichtigsten Grundsätzen.
    </p>
    <p>
        Unsere Mission ist das Anstoßen transformativer Lernprozesse.
        Prototypische Projekte dienen der Erprobung neuer Ansätze für das
        zukünftige Zusammenleben von Gesellschaft, Mensch und Natur.
    </p>


    <h2>Unsere Ziele</h2>
    <ol>
        <li>
            Gründung mehrerer Standorte in ländlichen Gebieten überall auf der Welt.
        </li>
        <li>
            Umsetzung inklusiver, ganzheitlicher und resilienter Modelle für ein
            nachhaltiges Leben im Einklang mit Natur und dem Umfeld.
        </li>
        <li>
            Konzepte für gemeinwohlorientierte Ökonomien,
            öko-soziale Lebens- und Unternehmensmodelle zu entwickeln und zu realisieren.
        </li>
        <li>
            Innovative, menschenorientierte Lernprozesse zu etablieren und
            transformatives Lernen zu ermöglichen und zu verbreiten.
        </li>
    </ol>
</div>
