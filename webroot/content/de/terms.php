<div class="text-column">

    <h2>§ 1 Firma, Sitz, Gegenstand</h2>
    <p>
        <strong>Firma:</strong> Die Genossenschaft heißt ThinkCamp gem. eG.<br>
    </p>
    <p>
        <strong>Sitz:</strong> Der Sitz der Genossenschaft ist Küstriner Vorland, Brandenburg.
    </p>
    <p>
        <strong>Gegenstand:</strong> ThinkCamp fördert ohne geographische Beschränkung die nachhaltige
        Entwicklung unserer Welt zu einer sinn- und wertorientierten ökosozialen, kulturellen und
        wirtschaftlichen Welt im Gleichgewicht.
        Das Angebot umfasst insbesondere:
    </p>
    <p>
        Coaching für die Entwicklung und Umsetzung von nachhaltigen Verbesserungen und
        Durchbruchs-Innovationen für Dienstleistungen, Produkte, Prozesse und Systeme.
        Bildungsprogramme in einem kreativen Lernumfeld für eine nachhaltige Entwicklung
        und einen Erfahrungsaustausch in Form von Gesprächen, Seminaren, Workshops, Wanderungen,
        Expeditionen und Camps.
        Forschung, Entwicklung und Publikationen zur Förderung von nachhaltigem Wirtschaften und Lernen.
        Aufbau regionaler und internationaler Netzwerke um die Kooperation und den Wissensaustausch
        zwischen den Mitgliedern, der Mitglieder mit Experten, anderen Netzwerken, Investoren und
        Dienstleistern zu fördern und den Zugang zu bestem Wissen und bester angewandter Praxis
        und Erfahrung zu erleichtern.
        Der Erwerb, die Bewirtschaftung, die Vermietung und Verpachtung von landwirtschaftlichen,
        forstwirtschaftlichen und fischereiwirtschaftlichen Flächen und Gebäuden und Gebäude für soziales
        Wohnen. Diese werden ausschließlich mit dem Ziel erworben, sie dauerhaft ökologisch, nachhaltig
        zu bewirtschaften, für soziale, gemeinnützige Zwecke zu nutzen und nicht zum Verkauf anzubieten.
        Als Eigentum der Genossenschaft sind sie der spekulativen Verwendung entzogen und dauerhaft
        zu entziehen. In besonderen Fällen sind die Veräußerung einzelner Grundstücke oder der Verkauf
        von Grundstücksteilen mit Zustimmung des Aufsichtsrats zulässig.
        Die gemeinsame Bewirtschaftung von land- und forstwirtschaftlichen Nutzflächen, die Erzeugung
        landwirtschaftlicher Produkte, deren Bearbeitung, Verarbeitung und Verzehr durch die Mitglieder
        die Erbringung von Dienstleistungen im ländlichen Raum, insbesondere Leistungen im Bereich
        der Landschaftsgestaltung und des Landschaftsschutzes sowie der Landschaftspflege.
        Die ökologische Erzeugung von erneuerbaren Energien zum Eigenverbrauch.
    </p>
    <p>
        Die Genossenschaft verfolgt ausschließlich und unmittelbar gemeinnützige Zwecke im Sinne des
        Abschnitts „Steuerbegünstigte Zwecke" der Abgabenordnung.
        Zweck der Körperschaft ist die Förderung von Wissenschaft und Forschung, Volks- und Berufsbildung
        und Umweltschutz.
        Der Satzungszweck wird verwirklicht insbesondere durch die Durchführung wissenschaftlicher
        Veranstaltungen und Forschungsvorhaben sowie der Förderung nachhaltigen Wirtschaftens.
        Die Körperschaft ist selbstlos tätig; sie verfolgt nicht in erster Linie eigenwirtschaftliche Zwecke.
        Mittel der Genossenschaft dürfen nur für die satzungsmäßigen Zwecke verwendet werden.
        Die Mitglieder erhalten keine Zuwendungen aus Mitteln der Genossenschaft.
        Es darf keine Person durch Ausgaben, die dem Zweck der Körperschaft fremd sind,
        oder durch unverhältnismäßig hohe Vergütungen begünstigt werden.
        Die Mitglieder dürfen keine Gewinnanteile und auch keine sonstigen Zuwendungen aus Mitteln
        der Genossenschaft erhalten. Sie erhalten bei ihrem Ausscheiden oder bei Auflösung der
        Genossenschaft oder bei Wegfall steuerbegünstigter Zwecke nicht mehr als ihre eingezahlten
        Kapitalanteile und den gemeinen Wert ihrer geleisteten Sacheinlagen zurück.
        Die Genossenschaft kann sich an anderen Unternehmen, die einen gemeinnützigen Zweck verfolgen,
        beteiligen und Zweigniederlassungen errichten.
        Geschäfte mit Nichtmitgliedern sind zulässig.
        Das Geschäftsjahr ist das Kalenderjahr. Im Jahr der Gründung wird ein Rumpfgeschäftsjahr gebildet.
    </p>

    <h2>§ 2 Mittelzuwendung, Mittelverwendung, Rücklagen</h2>
    <p>
        <strong>Zuwendung:</strong> Die Genossenschaft beschafft ihre Mittel durch Mitgliedsbeiträge,
        Spenden, Zuschüsse, Fördermittel, Gebühren für Bildungs- und Projektleistungen sowie
        durch Einnahmen aus Verpachtung und Vermietung.
    </p>
    <p>
        <strong>Verwendung:</strong> Die Mittel werden für die Durchführung von der Satzung
        entsprechenden Bildungs- und Betreuungsprogrammen, Stipendien, zur Finanzierung von
        Entwicklungsprojekten und zum Kauf von Grundstücken und Gebäuden verwendet.
    </p>
    <p>
        <strong>Rücklagen:</strong>Es ist eine gesetzliche Rücklage zu bilden in Höhe von
        20 % des Jahresüberschusses bis mindestens 100 % der Geschäftsguthaben erreicht sind.
        Verbleibende Überschüsse sind den anderen Ergebnisrücklagen zuzuführen.
    </p>

    <h2>§ 3 Liquidation, Auflösung, Wegfall steuerbegünstigter Zwecke</h2>
    <p>
        <strong>Liquidation:</strong> Die Auflösung der Genossenschaft erfolgt nach dem
        Genossenschaftsgesetz mit der Maßgabe, dass kein Mitglied mehr zurückerhalten darf,
        als es Einzahlungen auf den Geschäftsanteil geleistet hat.
    </p>
    <p>
        Bei <strong>Auflösung der Genossenschaft oder bei Wegfall</strong> steuerbegünstigter
        Zwecke fällt das Vermögen der Genossenschaft, soweit es die eingezahlten Kapitalanteile
        der Mitglieder und den gemeinen Wert der von den Mitgliedern geleisteten Sacheinlagen übersteigt,
        an die Global Marshall Plan Foundation, Hamburg zu, die es unmittelbar und ausschließlich
        für gemeinnützige, mildtätige oder kirchliche Zwecke oder für Bildung zu verwenden hat.
        Falls die Global Marshall Plan Foundation nicht mehr besteht oder nicht mehr als
        gemeinnützig anerkannt ist, fällt das Vermögen der Europäischen Union zwecks Verwendung
        für nachhaltige Bildung zu.
    </p>

    <h2>§ 4 Genossenschaftsanteile, Eintritt und Beiträge</h2>
    <p>
        Der Anteil beträgt 500 €. Er ist sofort in voller Höhe einzuzahlen.
        Eine natürliche Person muss sich mit mindestens einem Anteil beteiligen.
        Personengesellschaften oder juristische Personen müssen sich mit mindestens vier Anteilen beteiligen.
        Das Eintrittsgeld für eine natürliche Person wird in Höhe von 500 € festgelegt
        und wird der Kapitalrücklage zugeführt.
        Das Eintrittsgeld für Personengesellschaften oder juristische Personen wird in Höhe von
        2.000 € festgelegt und wird der Kapitalrücklage zugeführt.
        Es wird ein jährlicher Mitgliedsbeitrag erhoben. Die Generalversammlung beschließt
        über die Höhe des Beitrags und erlässt eine Beitragsordnung.
        Die Mitglieder sind nicht zur Leistung von Nachschüssen verpflichtet.
        Die Mitglieder erhalten keine Auszahlungen von Gewinnen.
    </p>

    <h2>§ 5 Mitgliedschaft</h2>
    <p>
        <strong>Wer:</strong> Die Mitgliedschaft können natürliche Personen,
        Personengesellschaften und juristische Personen des privaten oder öffentlichen Rechts erwerben.
    </p>
    <p>
        <strong>Wie</strong> wird man Mitglied: Die Mitgliedschaft wird erworben durch:
        die Unterzeichnung der Beitrittserklärung und Zeichnung der Geschäftsanteile,
        die Zulassung durch den Vorstand.
    </p>
    <p>
        <strong>Mitgliederliste:</strong> das Mitglied wird unverzüglich in die Mitgliederliste eingetragen
        und hiervon benachrichtigt.
    </p>
    <p>
        <strong>Kündigung:</strong> Die Mitgliedschaft endet durch Kündigung.
        Die Kündigungsfrist beträgt drei Jahre zum Schluss des Geschäftsjahres.
    </p>
    <p>
        <strong>Ausschluss:</strong> Mitglieder können ausgeschlossen werden:<br>
        Welche die Genossenschaft schädigen oder geschädigt haben.<br>
        Welche mehr als 6 Monate ihre monatlichen Mitgliedszahlungen nicht vorgenommen haben.<br>
        Dauerhaft nicht erreichbare Mitglieder können ausgeschlossen werden.
        Die Mitglieder sind verpflichtet der Genossenschaft jede Veränderung ihrer Anschrift
        oder ihrer Kontaktdaten inkl. E-Mail-Adresse unverzüglich mitzuteilen.<br>
        Welche ihren Verpflichtungen gegenüber der Genossenschaft nicht nachkommen.<br>
        Über den Ausschluss entscheidet der Vorstand.
        Gegen den Ausschließungs-Beschluss kann binnen vier Wochen nach Absendung bei
        der Generalversammlung Widerspruch eingelegt werden (Ausschlussfrist).
        Erst nach der Entscheidung der Generalversammlung kann der Ausschluss gerichtlich
        angefochten werden. Über den Ausschluss von Vorstandsmitgliedern oder Aufsichtsratsmitgliedern
        entscheidet die Generalversammlung.
    </p>
    <p>
        <strong>Tod:</strong> Mit dem Tod scheidet ein Mitglied aus.
        Seine Mitgliedschaft geht auf den Erben über. Sie endet mit dem Schluss des Geschäftsjahres,
        in dem der Erbfall eingetreten ist.
    </p>
    <p>
        <strong>Insolvenz:</strong> Wird über das Vermögen eines Mitglieds ein Insolvenzverfahren
        eröffnet oder die Eröffnung eines Insolvenzverfahrens mangels Masse abgelehnt,
        so endet die Mitgliedschaft mit dem Schluss des Geschäftsjahres, in dem das
        Insolvenzverfahren eröffnet oder die Eröffnung mangels Masse abgelehnt wurde.
        Wird eine juristische Person oder eine Personengesellschaft aufgelöst,
        so endet die Mitgliedschaft mit dem Schluss des Geschäftsjahres,
        in dem die Auflösung wirksam geworden ist.
        Ansprüche auf Auseinandersetzungsguthaben verjähren in zwei Jahren ab Fälligkeit.
        Die Beträge werden den Rücklagen zugeführt
    </p>

    <h2>§ 6 Generalversammlung</h2>
    <p>
        Die Generalversammlung wird mindestens einmal jährlich durch BekanntmaChung per Email
        und im Internet unter der Adresse der Genossenschaft einberufen. Sie hat innerhalb
        der ersten sechs Monate nach Ablauf des Geschäftsjahres stattzufinden.
        Die Einladung muss mindestens vier Wochen, Ergänzungen und Änderungen der Tagesordnung
        müssen mindestens zwei Woche vor der Generalversammlung erfolgen.
        Die Generalversammlung ist beschlussfähig, wenn mindestens drei Mitglieder anwesend sind.
        Jedes Mitglied hat eine Stimme.
        Die Generalversammlung beschließt eine Geschäftsordnung, die eine virtuelle
        Mitgliederversammlung via Internet auch für die Durchführung von Generalversammlungen vorsehen kann.
        Beschlüsse der Generalversammlung bedürfen der einfachen Mehrheit der abgegebenen Stimmen,
        soweit nicht das Gesetz oder diese Satzung eine größere Mehrheit vorschreibt.
        Beschlüsse werden gem. S 47 GenG protokolliert.
        Der Beschlussfassung der Generalversammlung unterliegen insbesondere:
        die Wahl der Mitglieder des Aufsichtsrates
        Entlastung des Vorstands und des Aufsichtsrats.
        Die Amtsdauer des Aufsichtsrates beträgt drei Jahre. Die Generalversammlung kann eine
        abweichende Amtsdauer festlegen. Eine Wiederwahl ist zulässig. Die Amtszeit beginnt mit
        der Generalversammlung, auf der die Wahl erfolgt, und sie endet mit der Generalversammlung,
        die in dem dritten Jahr nach der Wahl des Aufsichtsratsmitglieds stattfindet.
        Die Generalversammlung kann Mitglieder des Vorstandes mit zwei Dritteln der Stimmen der
        anwesenden Mitglieder abwählen.
        Den Tagungsort der Generalversammlung bestimmen Vorstand und Aufsichtsrat in gemeinsamer Sitzung.
        Den Vorsitz in der Generalversammlung führt der oder die Vorsitzende des Aufsichtsrats,
        sofern die Generalversammlung nichts anderes beschließt.
    </p>

    <h2>§ 7 Vorstand</h2>
    <p>
        Der Vorstand besteht aus mindestens zwei Mitgliedern.
        Solange nicht mehr als 20 Mitglieder vorhanden sind,
        ist die Bestellung von nur einem Vorstandsmitglied möglich.
        In diesem Fall wird die Genossenschaft von dem Vorstandsmitglied allein vertreten.
        Der Vorstand wird von den Mitgliedern, der Generalversammlung bestellt und abberufen.
        Der Aufsichtsrat ist für den Abschluss, die Änderung sowie die Beendigung von
        Dienstverträgen mit Vorstandsmitgliedern zuständig.
        Die regelmäßige Amtsdauer beträgt drei Jahre. Der Aufsichtsrat kann eine
        abweichende Amtsdauer festlegen. Eine Wiederwahl ist zulässig.
        Der Vorstand stellt eine Geschäftsordnung auf und beschließt diese einstimmig.
        Der Vorstand führt die Genossenschaft in eigener Verantwortung.
        Er bedarf der Zustimmung des Aufsichtsrates für:
    </p>
    <p>
        die Grundsätze der Geschäftspolitik,
        den Erwerb, die Bebauung, die Belastung und die Veräußerung von Grundstücken und
        grundstücksgleichen Rechten; ausgenommen ist der Erwerb von Grundstücken und
        grundstücksgleichen Rechten zur Rettung eigener Forderungen,
        Beteiligung an anderen Vereinigungen, Unternehmen einschließlich
        Abschlüssen von Kooperationsverträgen,
        den Abschluss von Verträgen mit besonderer Bedeutung, insbesondere von solchen
        Verträgen, durch die wiederkehrende Verpflichtungen in erheblichem Umfang
        für die Genossenschaft begründet werden,
        Erteilung von Prokura.
        Der Vorstand kann Beschlüsse schriftlich oder auf elektronischem Wege fassen.
        Die Vorstandsmitglieder sind einzelvertretungsberechtigt.
        Der Vorstand kann einen Beirat zu seiner Unterstützung einrichten.
        Der Vorstand erhält keine Vergütung.
    </p>

    <h2>§ 8 Aufsichtsrat</h2>
    <p>
        Der Aufsichtsrat überwacht und berät die Leitung der Genossenschaft und berichtet der
        Generalversammlung.
        Der Aufsichtsrat besteht aus mindestens drei Mitgliedern.
        Die Sitzungen des Aufsichtsrats sollen mindestens vierteljährlich stattfinden.
        Die Sitzungen können persönlich, telefonisch oder per Internetkonferenz durchgeführt werden.
        Der Aufsichtsrat ist beschlussfähig, wenn mindestens die Hälfte der Mitglieder an der
        Beschlussfassung teilnimmt.
        Der Aufsichtsrat kann schriftlich, telefonisch und auf elektronischem Wege Beschlüsse fassen,
        wenn kein Aufsichtsratsmitglied der Beschlussfassung widerspricht.
        Der Aufsichtsrat fasst seine Beschlüsse mit Mehrheit der Stimmen.
        Beschlüsse sind ordnungsgemäß zu protokollieren. Die Protokolle sind fortlaufend zu nummerieren.
        Der Aufsichtsrat erhält keine Vergütung.
    </p>

    <h2>§ 9 Bekanntmachungen</h2>

    <p>
        Gesetzlich vorgeschriebene Bekanntmachungen werden im elektronischen Bundesanzeiger veröffentlicht.
    </p>
    <p>
        Bekanntmachungen der Genossenschaft werden auf der Homepage veröffentlicht.
        Die Ankündigung der Generalversammlung wird im Internet unter der
        Adresse der Genossenschaft veröffentlicht.
    </p>
