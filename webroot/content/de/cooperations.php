    <h2>Deutschland</h2>

    <h3>EGOM - Energiegenossenschaft Oberes Mühlbachtal</h3>
    <p>
        UnaVision steht in Kontakt mit der Energiegenossenschaft Oberes Mühlbachtal.
        Durch die Kooperation fließt Erfahrungswissen für neue Bürgerenergiegenossenschaften,
        die nach dem Modell der EGOM gegründet werden können.
        Dezentrale Energieerzeugung, Speicherlösungen und Elektromobilität als
        Sharing-Konzept bringen die Energiewende in Reichweite. <br>
        <a href="https://egom.de/" target="_blank">egom.de</a>
    </p>

    <h3>Lehmhausen</h3>
    <p>
        Lehmhausen ist ein projektiertes Ökodorf aus nachwachsenden, kompostierbaren und natürlichen
        Rohstoffen wie Lehm und Holz. Das Projekt soll in Blankensee, Mecklenburg Vorpommern
        realisiert werden.<br>
        <a href="https://www.lehmhausen.de/" target="_blank">www.lehmhausen.de/</a>
