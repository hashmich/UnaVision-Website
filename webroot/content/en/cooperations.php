    <h2>Germany</h2>

    <h3>EGOM - Energy Cooperative Oberes Mühlbachtal</h3>
    <p>
        UnaVision is in contact with an energy cooperative in a region called Oberes Mühlbachtal.
        The cooperation makes experience available for new citizen cooperatives,
        that can be founded in a similar way.
        Decentralized power generation, energy storage and shared electric mobility concepts
        contribute towards energy transition.
        <br>
        <a href="https://egom.de/" target="_blank">egom.de</a>
    </p>

    <h3>Lehmhausen</h3>
    <p>
        Lehmhausen ist ein projektiertes Ökodorf aus nachwachsenden, kompostierbaren und natürlichen
        Rohstoffen wie Lehm und Holz. Das Projekt soll in Blankensee, Mecklenburg Vorpommern
        realisiert werden.

        Lehmhausen is a future eco-village in Blankensee, Mecklenburg Vorpommern.
        Housing designs consist of renewable, compostable and natural materials.<br>
        <a href="https://www.lehmhausen.de/" target="_blank">www.lehmhausen.de/</a>
    </p>


