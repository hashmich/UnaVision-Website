<div class="text-column">
    <h2>What</h2>
    <p>
        The objective is to create comprehensive space plans for the location.
        Concepts should consider residents, the new eco-village community and visitors,
        all working, living and learning together.
        A neighbouring 20ha land and nearby concrete slabs housing with 160 apartments
        should be considered in the results.
        The new eco-village will be built with natural materials, gardens follow permaculture principles.
        Resulting designs may comprise co-working spaces, workshops, a farmers market or regional café.
    </p>
    <h2>Who</h2>
    <p>
        Invited for the Vision-Lab are all interested people such as Architects,
        landscape designers, regional planners, artists, designers and
        finally visionaries of all fields.
        Participation is limited to about 20 people.
    </p>

